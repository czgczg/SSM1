package com.cskaoyan.vo;

public class ConsumptionVo {

    private String consumptionId;
    private String commodityName;
    private String commodityUOMName;
    private String commoditySalePrice;
    private String consumptionNumber;
    private String consumptionMoney;
    private String consumptionTime;


    public String getConsumptionId() {
        return consumptionId;
    }

    public void setConsumptionId(String consumptionId) {
        this.consumptionId = consumptionId;
    }

    public String getCommodityName() {
        return commodityName;
    }

    public void setCommodityName(String commodityName) {
        this.commodityName = commodityName;
    }

    public String getCommodityUOMName() {
        return commodityUOMName;
    }

    public void setCommodityUOMName(String commodityUOMName) {
        this.commodityUOMName = commodityUOMName;
    }

    public String getCommoditySalePrice() {
        return commoditySalePrice;
    }

    public void setCommoditySalePrice(String commoditySalePrice) {
        this.commoditySalePrice = commoditySalePrice;
    }

    public String getConsumptionNumber() {
        return consumptionNumber;
    }

    public void setConsumptionNumber(String consumptionNumber) {
        this.consumptionNumber = consumptionNumber;
    }

    public String getConsumptionMoney() {
        return consumptionMoney;
    }

    public void setConsumptionMoney(String consumptionMoney) {
        this.consumptionMoney = consumptionMoney;
    }

    public String getConsumptionTime() {
        return consumptionTime;
    }

    public void setConsumptionTime(String consumptionTime) {
        this.consumptionTime = consumptionTime;
    }
}
